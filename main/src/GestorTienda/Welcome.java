    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        String email = correo.getText();
        String myPass=String.valueOf(password.getPassword());
       
        Cliente client = new Cliente(email);
        client.password=myPass;
        
        Connection conn = null;
        try {
            conn = DriverManager.getConnection(CONN_STRING, USERNAME, PASSWORD);
            String sql = "SELECT * FROM users WHERE correo = ? AND password = ?";
            int n = 0;
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, client.correo);
            ps.setString(2, client.password);
            ps.execute();
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                n++;
            } if (n > 0){
                if (vistaCliente.isSelected()){
                    ps.close();
                    conn.close();
                    VistaUsuario userView = new VistaUsuario(email);
                    userView.show();
                    dispose();
                    JOptionPane.showMessageDialog(null, "Actualizar saldo");
                } else{
                    ps.close();
                    conn.close();
                    VistaTrabajador workerView = new VistaTrabajador(email);
                    workerView.show();
                    dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario y/o Password inv√°lido/s");
            }
            ps.close();
            ps=null;
            conn.close();
        } catch(SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_LoginActionPerformed

    private void SignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignupActionPerformed
        String email = correo.getText();
        String myPass=String.valueOf(password.getPassword());
        
        Trabajador worker = new Trabajador(email);
        worker.password = myPass;
        
        Connection conn = null;

        try {
            conn = DriverManager.getConnection(CONN_STRING, USERNAME, PASSWORD);
            Statement stmt = (Statement) conn.createStatement();
            String insert = "INSERT INTO gestor_tienda.users(correo, password) VALUES (?,?)";
            PreparedStatement ps = conn.prepareStatement(insert);
            ps.setString(1, worker.correo);
            ps.setString(2, worker.password);
            ps.execute();
            ps.close();
            ps=null;
            conn.close();
            JOptionPane.showMessageDialog(null, "Usuario creado exitosamente");
            correo.setText("");
            password.setText("");
        } catch (SQLException e){
            System.err.println(e);
            JOptionPane.showMessageDialog(null, "Usuario existente.");
        }
        
    }//GEN-LAST:event_SignupActionPerformed

