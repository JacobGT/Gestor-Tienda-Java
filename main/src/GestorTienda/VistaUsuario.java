    private void comprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comprarActionPerformed
        // TODO add your handling code here:     
        if (confirm.isSelected()){
            int precio = Integer.parseInt(priceConf.getText());
            String saldoFinal = Integer.toString(Integer.parseInt(saldo.getText()) - precio);
            saldo.setText(saldoFinal);
            JOptionPane.showMessageDialog(null, "Compra efectuada exitosamente. ¡Gracias!");
            
            Connection conn = null;
            String sql = "UPDATE users SET saldo=? WHERE correo=?";
            try {
                conn = DriverManager.getConnection(CONN_STRING, USERNAME, PASSWORD);
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setInt(1, Integer.parseInt(saldoFinal));
                ps.setString(2, (CustomId.getText()));
                ps.execute();
                ps.close();
                ps=null;
                conn.close();
            } catch (SQLException e) {
                System.out.println(e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Confirmacion no ha sido seleccionada.");
        }
    }//GEN-LAST:event_comprarActionPerformed

