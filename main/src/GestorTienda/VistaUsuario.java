    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String email = CustomId.getText();
        Connection conn = null;
        try {
            conn = DriverManager.getConnection(CONN_STRING, USERNAME, PASSWORD);
            String sql = "SELECT * FROM users WHERE correo = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, email);
            ps.execute();
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                saldo.setText(Integer.toString(rs.getInt("saldo")));
                if (rs.getInt("saldo") <= 0){
                    JOptionPane.showMessageDialog(null, "Saldo igual o menor a cero.");
                    Dinero.setText("Deuda");
                } else {
                    Dinero.setText("Saldo");
                }
            }
            ps.close();
            ps=null;
            conn.close();
        } catch(SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

